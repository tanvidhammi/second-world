class SwitchDemo
{
public static void main(String args[])
	{
		int t=20;
		switch(t)
	{
	case 1:System.out.println("is equal to 1");
	break;
	case 2:System.out.println("is equal to 2");
	break;
	case 3:System.out.println("is equal to 3");
	break;
	default:System.out.println("is not 1,2,3");
	break;
	}
	}
}